class Solution
{
    public:
        vector <int> search(string pattern, string text)
        {
          vector<int>ans;
            int n=pattern.length();
            int i=0,j=n-1;
            int m=text.length();
            while(j<m){
                if(text.substr(i,j-i+1)==pattern)ans.push_back(i+1);
                i++;j++;
            }
            return ans;
        }
     
};

//{ Driver Code Starts.
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        string S, pat;
        cin >> S >> pat;
        Solution ob;
        vector <int> res = ob.search(pat, S);
        for (int i : res) cout << i << " ";
        cout << endl;
    }
    return 0;
}

